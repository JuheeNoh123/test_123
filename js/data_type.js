//정수/소수, 문자열, bool 타입
let number = 5;
let str = '문자열 입력' //""도 묶음 가능
let prime = 1.5123;
let is_ok = true;	//참
let is_not = false; //거짓
let undefi;	//변수 이름만, 초기화 X
let empty = null;	//비어있음

console.log(number);	//5
console.log(str);		//문자열 입력
console.log(prime);		//1.5123
console.log(is_ok);		//true
console.log(is_not);	//false
console.log(undefi);	//undefined
console.log(empty);		//null
console.log(number,str);	//여러개 출력가능
